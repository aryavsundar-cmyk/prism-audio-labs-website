export const effectsSynthsCollections = collections.filter(c => c.group === 'effects-synths');
export const worldCollections = collections.filter(c => c.group === 'world');

export function getCollectionById(id: string): Collection | undefined {
  return collections.find(c => c.id === id);
}

export function getPluginById(id: string): { plugin: Plugin; collection: Collection } | undefined {
  for (const collection of collections) {
    const plugin = collection.plugins.find(p => p.id === id);
    if (plugin) return { plugin, collection };
  }
  return undefined;
}

